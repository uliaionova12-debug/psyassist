export interface IntegrationReflectionAnswerItem {
  module: string;
  question: string;
  answer: string;
}

export function build_integration_reflection_prompt(
  caseText: string,
  supervisionRequest: string,
  allAnswers: IntegrationReflectionAnswerItem[],
  clinicalBrain: string,
  supervisorStyleLabel: string | null
): string {
  let qnaText = "";
  for (const item of allAnswers) {
    qnaText +=
      `\n[${item.module}]\n` +
      `Вопрос: ${item.question}\n` +
      `Ответ: ${item.answer}\n`;
  }

  const styleLine =
    supervisorStyleLabel?.trim() ||
    "не указан — держи нейтральный уважительный тон опытного коллеги-супервизора";

  return (
    `${clinicalBrain}\n\n===\n\n` +
    "Ты — опытный клинический супервизор. Только что завершилась супервизорская сессия.\n" +
    "Терапевт ответил на вопросы по случаю. Ниже — весь материал.\n\n" +
    `Клинический случай:\n${caseText}\n\n` +
    `Запрос терапевта:\n${supervisionRequest || "Не уточнён"}\n\n` +
    `Вопросы и ответы терапевта:\n${qnaText}\n\n` +
    "ИМПРИНТ СТИЛЯ СУПЕРВИЗОРА (обязательный слой, не формальность):\n" +
    `Выбранная позиция супервизора в интерфейсе: «${styleLine}».\n` +
    "Удерживай её как голос, темп и «лицо» всего ответа: степень прямоты vs мягкость, допустимая ирония, акцент на этике контакта или на тактике, скорость мышления. " +
    "Читатель должен слышать именно этот стиль в каждом абзаце — не только во вступительной фразе.\n\n" +
    "ОБЪЁМ И ПЛОТНОСТЬ:\n" +
    "Для этой интеграционной рефлексии игнорируй внешнее ограничение «700–1200 символов», если оно пришло из общего базового блока выше. " +
    "Цель — сильный завершённый текст примерно 1800–2800 символов: плотно, без воды, без повторов одной мысли разными словами.\n\n" +
    "Напиши итоговую супервизорскую рефлексию в режиме прорыва (не «резюме», а честное клиническое попадание).\n\n" +
    "СТРУКТУРА ОТВЕТА — строго 6 блоков с ТОЧНЫМИ заголовками ниже (каждый заголовок — отдельная строка; без нумерации). " +
    "После каждого заголовка с новой строки иди текст раздела.\n\n" +
    "Что вы, возможно, уже видите, но пока не называете\n" +
    "Дай 3–5 предложений. Это не обзор, а прямое называние узла. " +
    "Обязательно включи минимум одну клиническую гипотезу в **жирном** (единственное допустимое markdown-выделение во всём ответе — только для этой гипотезы в этом блоке). " +
    "Эта гипотеза — про слепые зоны терапевта: спасательство/переработка/перенос-контрперенос/разыгрывание привязанности/повторение динамики. " +
    "Тон соответствует выбранному стилю супервизора и остаётся этически бережным к терапевту, но достаточно точным, чтобы было «щёлкнуло». Не академический.\n\n" +
    "Где симптом может быть умнее терапии\n" +
    "Отдельный раздел с именно этим заголовком — без перефразирования заголовка. " +
    "Дай 2–4 предложения: 1–2 неожиданные радикальные переформулировки функции симптома (защищает, оттягивает близость, регулирует лишение, разряжает злость, сохраняет дистанцию, удерживает идентичность стыда и т. п.). " +
    "Подчеркни интеллект симптома как попытку справиться — без морализаторства и без обесценивания страдания.\n\n" +
    "Где терапевт мог быть приглашён в знакомую клиентке динамику\n" +
    "Опиши участие терапевта в петле: где он мог начать спасать/доказывать/работать больше клиента/становиться недоступным объектом/зарабатывать доверие. " +
    "Если в ответах есть данные контрпереноса (раздражение, вина, жалость, азарт, усталость, страх ошибки, желание «сделать правильно») — используй их прямо.\n\n" +
    "Если эта клиентка вернётся\n" +
    "Начни этот блок с подписи на отдельной строке ровно так: «Микро-стратегия следующей сессии». " +
    "Под ней дай ровно 3 конкретных микро-шага на ближайшую встречу: каждый шаг — это действие терапевта плюс готовая формулировка короткой фразы или вопроса (не общие лозунги). " +
    "Шаги должны быть выполнимы за одну сессию.\n\n" +
    "Один супервизионный удар\n" +
    "Одна короткая фраза на 8–14 слов: высокая концентрация смысла, эффект «попало», без клише и без морали.\n\n" +
    "Фраза на память\n" +
    "Одна короткая запоминающаяся строка (до ~120 символов), которую терапевту можно буквально унести с собой или пересказать коллеге одним сообщением. " +
    "Без хештегов, без спама символами, без цепочки восклицаний, без рекламного тона.\n\n" +
    "СТИЛЬ И ОГРАНИЧЕНИЯ:\n" +
    "— Пиши по-русски. Обращайся к терапевту на «вы».\n" +
    "— Не используй слова: «возможно», «скорее всего», «может быть», «вероятно», «похоже», «кажется».\n" +
    "— Никаких приветствий, комплиментов, дисклеймеров, образовательных лекций.\n" +
    "— Никаких диагнозов клиенту и никаких предписаний, которые подталкивают к вреду себе или другим.\n" +
    "— Никаких ссылок на правила формата. Просто выдай 6 блоков под указанными заголовками."
  );
}
