export const SUPERVISION_TRANSITIONS = [
  "Принято.",
  "Понял.",
  "Продолжим собирать клиническую картину.",
  "Это помогает точнее определить вектор терапии.",
] as const;

export function is_narrative_sufficient(narrative: string): boolean {
  const words = narrative.split(/\s+/).filter(Boolean);

  if (words.length < 40) {
    return false;
  }

  const text = narrative.toLowerCase();
  let markers = 0;

  if (["сессий", "встреч", "встреча", "сеанс", "раз работа"].some((w) => text.includes(w))) {
    markers += 1;
  }
  if (["подход", "метод", "техник", "интервенц", "пробовал", "использу"].some((w) => text.includes(w))) {
    markers += 1;
  }
  if (
    ["динамик", "изменил", "улучшил", "ухудшил", "прогресс", "стал", "начал"].some((w) =>
      text.includes(w)
    )
  ) {
    markers += 1;
  }
  if (
    ["чувствую", "напряжен", "тревог", "сложн", "трудн", "цепляет", "беспокоит"].some((w) =>
      text.includes(w)
    )
  ) {
    markers += 1;
  }
  if (
    ["запрос", "проблем", "симптом", "жалоб", "обратил", "говорит"].some((w) => text.includes(w))
  ) {
    markers += 1;
  }

  if (words.length < 80) {
    return markers >= 3;
  }

  return markers >= 2;
}

export function pickSupervisionTransition(): string {
  const i = Math.floor(Math.random() * SUPERVISION_TRANSITIONS.length);
  return SUPERVISION_TRANSITIONS[i] ?? SUPERVISION_TRANSITIONS[0];
}
